# Comparative-Cohort-Analysis-Organic-vs-Promo-
Порівняльний когортний аналіз Retention Rate (SQL &amp; Google Sheets)
Опис проєкту
Аналіз утримання користувачів (Retention) для оцінки ефективності різних каналів залучення. Порівняння поведінки груп "Organic" та "Promo".

## Інструменти
* **SQL (PostgreSQL):** сегментація даних, робота з датами, агрегація.
* **Google Sheets:** Pivot Tables, Heatmaps.

## Що було зроблено
1. Вивантажено дані з БД за допомогою SQL-запиту (використано JOIN та CTE).
2. Сформовано когорти за місяцем першої активності.
3. Розраховано матрицю Retention Rate для кожного сегмента.
4. Побудовано теплову карту для візуалізації відтоку користувачів.

