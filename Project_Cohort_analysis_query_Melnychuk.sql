##  Ключовий висновок
Канал ORGANIC показує значно краще утримання в порівнянні із загальним на всіх етапах, 
- зокрема, загальний "відтік" за 5 місяців складає лише 44% (порівняно із загальними 60%), тобто аудиторія є більш стабільною,
-що говорить про значно краще утримання / задоволення користувачів / відповідністю продукту очікуванням споживачів, які прийшли органічно.
- для 3-ої когорти цікавим є 2ий період після реєстрації, коли retention_rate різко збільшується з 80% до 92%. Важливо дослідити події, що могли спровокувати такий "стрибок"
- загалом канал organic є більш стабільним, а користувачі більш лояльними до продукту
